package com.medicoapp.filtros;

public class FiltroSituacao {

	private String especialidadeSituacao;
	
	private String medicoSituacao;

	public String getEspecialidadeSituacao() {
		return especialidadeSituacao;
	}

	public void setEspecialidadeSituacao(String especialidadeSituacao) {
		this.especialidadeSituacao = especialidadeSituacao;
	}

	public String getMedicoSituacao() {
		return medicoSituacao;
	}

	public void setMedicoSituacao(String medicoSituacao) {
		this.medicoSituacao = medicoSituacao;
	}
	

}
